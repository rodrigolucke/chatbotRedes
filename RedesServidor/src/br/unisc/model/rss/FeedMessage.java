package br.unisc.model.rss;

/**
 * Represents one RSS message
 */
public class FeedMessage {

	/**
	 * titulo da noticia
	 */
	String title;
	/**
	 * descricao da noticia
	 */
	String description;
	/**
	 * link da noticia
	 */
	String link;
	/**
	 * autor da noticia
	 */
	String author;

	String guid;

	/**
	 * retorna o titula da noticia
	 * 
	 * @return title
	 */
	public String getTitle() {
		return title;
	}

	/**
	 * Configura o titulo da noticia
	 * 
	 * @param title
	 */
	public void setTitle(String title) {
		this.title = title;
	}

	/**
	 * retorna a descricao da noticia
	 * 
	 * @return description
	 */
	public String getDescription() {
		return description;
	}

	/**
	 * configura a descricao da noticia
	 * 
	 * @param description
	 */
	public void setDescription(String description) {
		this.description = description;
	}

	/**
	 * retorna o link da noticia
	 * 
	 * @return link
	 */
	public String getLink() {
		return link;
	}

	/**
	 * configura o link da noticia
	 * 
	 * @param link
	 */
	public void setLink(String link) {
		this.link = link;
	}

	/**
	 * retorna o autor da noticia
	 * 
	 * @return author
	 */
	public String getAuthor() {
		return author;
	}

	/**
	 * configura o autor da noticia
	 * 
	 * @param author
	 */
	public void setAuthor(String author) {
		this.author = author;
	}

	public String getGuid() {
		return guid;
	}

	public void setGuid(String guid) {
		this.guid = guid;
	}

	@Override
	public String toString() {
		return "FeedMessage [title=" + title + ", description=" + description + ", link=" + link + ", author=" + author
				+ ", guid=" + guid + "]";
	}

}